package com.rax.iaam.Model;

public class HierarchieModel {
    private int id;
    private String name;

    public HierarchieModel(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
